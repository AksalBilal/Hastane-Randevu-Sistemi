package com.mehmetaydin.hastanerandevusistemi.Model;

public class Favori {
    private String favoriID;
    private String doktorID;
    private String hastaID;
    private String hastaneID;

    public String getFavoriID() {
        return favoriID;
    }

    public void setFavoriID(String favoriID) {
        this.favoriID = favoriID;
    }

    public String getDoktorID() {
        return doktorID;
    }

    public void setDoktorID(String doktorID) {
        this.doktorID = doktorID;
    }

    public String getHastaID() {
        return hastaID;
    }

    public void setHastaID(String hastaID) {
        this.hastaID = hastaID;
    }

    public String getHastaneID() {
        return hastaneID;
    }

    public void setHastaneID(String hastaneID) {
        this.hastaneID = hastaneID;
    }
}
